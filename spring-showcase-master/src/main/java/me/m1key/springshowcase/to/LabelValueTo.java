package me.m1key.springshowcase.to;

public class LabelValueTo {

	private String label;
	private String value;

	public LabelValueTo(String label, String value) {
		super();
		this.label = label;
		this.value = value;
	}

	public String getLabel() {
		return label;
	}

	public String getValue() {
		return value;
	}

}
